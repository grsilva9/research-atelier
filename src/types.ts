export interface Task {
  id: string;
  paperId: PaperId;
  stage: Stage;
  title: string;
  status: TaskStatus;
  source: 'manual' | 'suggested';
  /** Why the rules engine suggested this. Empty for manual tasks. */
  rationale?: string;
  createdAt: string;
  completedAt?: string;
  /** Optional free-form notes on the task itself. */
  notes?: string;
  isFocus?: boolean;
  /**
   * MCDM criteria used to compute the task's utility score.
   * Required for all v3 tasks; migrated from defaults for older schemas.
   */
  criteria: TaskCriteria;
}

/**
 * Paper — a research project. Does NOT have a single stage. Accumulates
 * work in the form of tasks across all six stages.
 */
export interface Paper {
  id: PaperId;
  title: string;
  shortName: string;
  venue?: string;
  deadline?: string;
  /** Per-stage manual overrides. Auto-completion is computed from tasks. */
  stageStatus: Record<Stage, StageStatus>;
  /** Free-form conceptual notes — one per ideation sub-stage. */
  ideationNotes: Record<IdeationSubstage, IdeationNote>;
  createdAt: string;
  updatedAt: string;
  links: { label: string; url: string }[];
  collaborators: string;
  /** If true, the paper is paused/shelved — hidden from active views. */
  paused: boolean;
  /** If true, the paper is submitted or published — shown but deprioritised. */
  archived: boolean;
}

/**
 * Diary entry — timestamped reflection. Can be attached to a paper or
 * stand alone.
 */
export interface DiaryEntry {
  id: string;
  paperId?: PaperId;
  kind: 'log' | 'weekly-review';
  content: string;
  createdAt: string;
}

/**
 * The entire serializable application state.
 */
export interface AppState {
  schemaVersion: 3;
  papers: Paper[];
  tasks: Task[];
  diary: DiaryEntry[];
  settings: {
    /** Max tasks in 'in_progress' status across all papers. */
    wipLimit: number;
    /** Days of inactivity before a paper is flagged as stale. */
    staleDays: number;
  };
  lastSavedAt: string;
}
