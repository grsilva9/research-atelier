/**
 * Per-ideation-substage note: free-form text plus a 0–4 maturity score
 * indicating how clear the idea is in the author's head.
 *
 *   0 Empty    — nothing there yet
 *   1 Vague    — rough intuition, not articulable
 *   2 Drafted  — can explain it but it has holes
 *   3 Solid    — could defend it in a meeting
 *   4 Defended — survived adversarial pushback or review
 */
export type MaturityLevel = 0 | 1 | 2 | 3 | 4;

export const MATURITY_LABELS: Record<MaturityLevel, string> = {
  0: 'Empty',
  1: 'Vague',
  2: 'Drafted',
  3: 'Solid',
  4: 'Defended',
};

export interface IdeationNote {
  maturity: MaturityLevel;
  notes: string;
}

/**
 * Per-paper, per-stage completion status.
 *
 * `auto` is derived from the paper's tasks in that stage (done / total).
 * `manualDone` is an explicit user override; when true, the stage is
 * considered complete regardless of task counts. It is STICKY — adding a
 * new open task to a manually-completed stage does not un-mark it, but
 * the UI will surface the disagreement.
 */
export interface StageStatus {
  manualDone: boolean;
}

/**
 * Task — the unit of work. Lives on the Kanban board.
 *
 *   status: 'todo' | 'in_progress' | 'done'
 *     'in_progress' tasks count toward the WIP limit.
 *
 *   source: 'manual' | 'suggested'
 *     Suggested tasks were generated by the rules engine and accepted by
 *     the user. They become ordinary tasks once accepted.
 *
 *   isFocus: boolean
 *     At most one task in the whole app holds focus = true. The Focus
 *     view foregrounds it over algorithmic suggestions.
 */
export type TaskStatus = 'todo' | 'in_progress' | 'done';

export const TASK_STATUS_LABELS: Record<TaskStatus, string> = {
  todo: 'To do',
  in_progress: 'In progress',
  done: 'Done',
};

/**
 * Task criteria — the six dimensions of the Multi-Criteria Decision Making
 * utility model used to prioritize tasks.
 *
 * The utility function is:
 *    U = (V * P * (1 + I) * T) / (C + S)
 *
 * where S (switch cost) is computed on demand from the currently-focused
 * task's paper/stage, and is NOT stored on the task itself.
 *
 * Scales are small, discrete, and calibrated against each other. Not
 * empirically derived — the point is to make the axes explicit so
 * tradeoffs are visible.
 *
 * Grounded in: Expected Utility Theory, WSJF (Weighted Shortest Job First),
 * temporal discounting, cognitive load / task-switching literature.
 */
export type Impact = 0.3 | 0.6 | 1.0;
export type Probability = 0.4 | 0.7 | 0.9;
export type InfoGain = 0 | 0.5 | 1.0;
export type TimeSensitivity = 0.3 | 0.6 | 1.0;
export type Effort = 1 | 2 | 3;

export interface TaskCriteria {
  /** V — how much does this move the paper toward acceptance? */
  impact: Impact;
  /** P — how likely is this task to succeed? */
  probability: Probability;
  /** I — how much does completing this reshape what to do next? */
  infoGain: InfoGain;
  /** T — how much does delay reduce the value? */
  timeSensitivity: TimeSensitivity;
  /** C — time / energy cost. */
  effort: Effort;
}

