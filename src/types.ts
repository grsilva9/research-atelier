/**
 * Core domain types for the research tracker.
 *
 * The mental model:
 *
 *   - A Paper is a long-running project. It does NOT advance through stages.
 *     It accumulates work across six parallel, non-linear stages.
 *
 *   - A Stage is a CATEGORY OF WORK: ideation, literature, drafting, code,
 *     experiments, aesthetics. At any moment, a paper can have active work
 *     in several stages at once.
 *
 *   - A Task is the unit of work. Each task belongs to exactly one stage and
 *     one paper. Tasks are what moves across the Kanban board — NOT papers.
 *
 *   - Per-paper stage completion is computed automatically from the tasks
 *     in that stage, but the user can manually mark a stage complete to
 *     override the auto-computed value.
 *
 *   - Ideation is special: it has seven sub-stages corresponding to the
 *     conceptual parts of a paper (Title, Gap, Hypothesis, ...). These
 *     live as free-form notes on the paper — they are the "thinking layer"
 *     beneath the task layer.
 */

export type PaperId = string;

/** The six top-level stages. Non-linear; a paper can have tasks in any subset. */
export const STAGES = [
  'ideation',
  'literature',
  'drafting',
  'code',
  'experiments',
  'aesthetics',
] as const;

export type Stage = (typeof STAGES)[number];

